package com.example.vgproject2.entity;

public class Demo {

    private String stakeholder;
    private String save_old_data;
    private String tickerfile;
    private String path;

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getStakeholder() {
        return stakeholder;
    }

    public void setStakeholder(String stakeholder) {
        this.stakeholder = stakeholder;
    }

    public String getSave_old_data() {
        return save_old_data;
    }

    public void setSave_old_data(String save_old_data) {
        this.save_old_data = save_old_data;
    }

    public String getTickerfile() {
        return tickerfile;
    }

    public void setTickerfile(String tickerfile) {
        this.tickerfile = tickerfile;
    }
}
